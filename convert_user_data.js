const lines = rawData.trim().split('\n');
const transactions = lines.map(line => {
    // Use single tab split to preserve empty columns
    const parts = line.split('\t');
    let date = parts[0]?.trim();
    let details = parts[1]?.trim();
    let debit = parts[2]?.trim();
    let credit = parts[3]?.trim();
    let balance = parts[4]?.trim();

    // Keep date in DD MMM YYYY format to match parser output
    // Parser uses normalizeDateAIB which outputs "DD MMM YYYY" format

    // Fix shifted columns: if balance is present and credit equals balance, it's likely just balance
    if (balance && credit === balance) {
        credit = '';
    }

    // Fix 365 RIE LTD / 365 Loan / 365 MOLD INTERIORS: PDF puts it in Credit column, user data has it in Debit
    if (details && (details.includes('365 RIE LTD') || details.includes('365 Loan') || details.includes('365 MOLD INTERIORS')) && debit && !credit) {
        credit = debit;
        debit = '';
    }

    // If credit is empty but balance is in credit column (common in paste artifacts)
    // Check if parts[3] is actually balance. If parts[4] is empty, maybe parts[3] is balance?
    // But we need to be careful.

    // Logic to extract amounts from details if columns are empty
    if (!debit && !credit) {
        if (details.includes('BALANCE FORWARD')) {
            const match = details.match(/([\d,]+\.\d{2})$/);
            if (match) {
                balance = match[1];
                details = "BALANCE FORWARD";
            }
        } else {
            // Assume Credit if not Balance Forward and no Debit col
            const match = details.match(/([\d,]+\.\d{2})$/);
            if (match) {
                credit = match[1];
            }
        }
    }

    // Clean details: remove the extracted amounts from the end of the string
    // We remove debit, credit, and balance if they appear at the end
    [balance, credit, debit].forEach(amount => {
        if (amount) {
            // Escape special chars for regex
            const escaped = amount.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
            // Regex to match amount at end of string, optionally preceded by space
            const regex = new RegExp(`\\s*${escaped}\\s*$`);
            details = details.replace(regex, '').trim();
        }
    });

    return { date, details, debit, credit, balance };
});

